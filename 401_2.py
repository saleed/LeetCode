class Solution:
    def readBinaryWatch(self, num: int):

        if num==0:
            return ["0:00"]

        state=[0]*10
        res=[]
        self.dfs(10,num,0,state,res)

        fres=[]
        for i in res:
            cur=self.decode(i)
            if cur =="": #12:00跟0:00重合
                continue
            fres.append(cur)
        return fres




    def dfs(self,n,left,id,state,res):
        print(state)
        if left==0:
            res.append(state[:])
            return
        if id>=n:
            return
        self.dfs(n,left,id+1,state,res)
        state[id]=1
        self.dfs(n,left-1,id+1,state,res)
        state[id]=0



    def decode(self,state):
        hour=0
        minit=0
        for i in range(4):
            hour+=state[i]*pow(2,i)
        for j in range(4,len(state)):
            minit+=state[j]*pow(2,j-4)

        if hour>=12 or minit>=60:
            return ""

        if minit<10:
            minit='0'+str(minit)

        res=str(hour)+":"+str(minit)



        return res


